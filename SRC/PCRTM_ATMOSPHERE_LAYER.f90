  TYPE PCRTM_GEOMETRY_TYPE
     REAL(SINGLE) :: PSFC
     REAL(SINGLE) :: POBS
     INTEGER      :: LSFC
     INTEGER      :: LOBS
     INTEGER      :: NTOP
     INTEGER      :: NBOT
     REAL(SINGLE) :: SCALSFC
     REAL(SINGLE) :: SCALOBS
     REAL(SINGLE) :: SECZANG  
     REAL(SINGLE) :: SATANG   
