! SUBROUTINE LinXLinP
!#############################################################################       
  subroutine linXlinP(x1,x2,y1,y2,y,x)
      IMPLICIT NONE
      
!---  Subroutine arguments
      REAL(SINGLE), INTENT(IN)  :: x1
      REAL(SINGLE), INTENT(IN)  :: x2
      REAL(SINGLE), INTENT(IN)  :: y1
