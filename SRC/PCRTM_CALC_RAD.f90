MODULE PCRTM_CALC_RAD

  USE PCRTM_TYPE_KIND
  USE PCRTM_MATH_UTILITY
  USE PCRTM_ATM_ABSORPTION_DEFINE
  USE PCRTM_JACOBIAN
  USE PCRTM_CLOUD_DEFINE
